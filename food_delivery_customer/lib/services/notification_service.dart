import 'package:cloud_firestore/cloud_firestore.dart';
import 'package:firebase_auth/firebase_auth.dart';

class NotificationService {
  final FirebaseFirestore _fireStore = FirebaseFirestore.instance;

  Future<void> saveFcmToken(String token) async {

    final user = FirebaseAuth.instance.currentUser;
    if (user == null) return;

    final docRef = _fireStore.collection('users').doc(user.uid);

    try {
      // üîç T√¨m t·∫•t c·∫£ user ƒëang gi·ªØ token n√†y
      final query = await _fireStore
          .collection('users')
          .where('fcmToken', isEqualTo: token)
          .get();

      // ‚ùå X√≥a token ·ªü user c≈© (n·∫øu kh√¥ng ph·∫£i user hi·ªán t·∫°i)
      for (var doc in query.docs) {
        if (doc.id != user.uid) {
          await doc.reference.update({'fcmToken': FieldValue.delete()});
          print('üßπ Removed FCM token from old user: ${doc.id}');
        }
      }

      // üìã Ki·ªÉm tra token hi·ªán t·∫°i trong doc
      final snapshot = await docRef.get();
      final existingToken = snapshot.data()?['fcmToken'];

      if (existingToken == token) {
        print('‚ÑπÔ∏è FCM token unchanged ‚Äî skip update.');
        return;
      }

      // ‚úÖ G√°n token m·ªõi cho user
      await docRef.set({'fcmToken': token}, SetOptions(merge: true));
      print('‚úÖ FCM token assigned to user ${user.uid}');
    } catch (e) {
      print('‚ùó Failed to save/reassign FCM token: $e');
    }
  }
}